import java.io.FileNotFoundException;
import java.lang.SecurityException;
import java.util.Formatter;
import java.util.FormatterClosedException;
import java.util.NoSuchElementException;
import java.util.Scanner;


public class CreateTextFile {
	private static Formatter output;

	public static void main(String[] args) {
		openFile();
		addRecords();
		closeFile();
	}
	
	public static void openFile(){	//一定要做Exception處理
		try{
			output = new Formatter("clients.txt");
		}
		catch(SecurityException securityException){	//沒有權限
			System.err.println("Write permission denied. Terminating.");
			System.exit(1);
		}
		catch(FileNotFoundException fileNotFoundException){	//沒辦法開啟檔案
			System.err.println("Error opening file. Terminating.");
			System.exit(1);
		}
	}
	public static void addRecords(){
		Scanner input = new Scanner(System.in);
		System.out.printf("%s\n%s\n?",
				"Enter account number, first name, last name and balance.",
				"Enter end-of-file indicator to end input.");
		
		while(input.hasNext()){
			try{
				//output new record to file
				output.format("%d %s %s %.2f\n", 
						input.nextInt(), input.next(), input.next(), input.nextDouble());
				
			}
			catch(FormatterClosedException formatterClosedException ){	//formatter已被關閉
				System.err.println("Error writing to file. Terminating.");
				break;
			}
			catch(NoSuchElementException noSuchElementException){	//if no more tokens are available
				System.err.println("Invalid input. Please try again.");
				input.nextLine();	//discard input so user can try again
			}
			System.out.print("? ");
		}
	}
	public static void closeFile(){
		if(output!=null)
			output.close();
	}

}
